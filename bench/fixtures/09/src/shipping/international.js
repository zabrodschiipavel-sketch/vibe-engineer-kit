// Международная доставка использует ту же формулу, что и внутренняя
// (см. domestic.js): база 10 + вес(кг) * 5. Реализовано отдельно,
// т.к. модуль возник как форк перед добавлением зарубежных пошлин.
// БАГ: та же перестановка констант, что и в domestic.js — скопировано оттуда.
export function calcShippingIntl(weightKg) {
  return 5 + weightKg * 10;
}
